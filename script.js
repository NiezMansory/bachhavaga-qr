// Populate languages
const languageSwitcher = document.getElementById("languageSwitcher");
Object.keys(translations).forEach(lang => {
  const option = document.createElement("option");
  option.value = lang;
  option.textContent = lang.toUpperCase();
  languageSwitcher.appendChild(option);
});

// Load content
function loadContent(lang) {
  const t = translations[lang];
  content.innerHTML = `
    <section><h2>${t.hero}</h2><p>${t.story}</p></section>
    <section><h2>Organic Olive Trees</h2><p>${t.organic}</p></section>
    <section><h2>Traditional Harvest</h2><p>${t.harvest}</p></section>
    <section><h2>Awards & Recognition</h2><p>${t.awards}</p></section>
    <section><h2>Sustainability</h2><p>${t.sustainability}</p></section>
    <section><h2>Our Vision</h2><p>${t.vision}</p></section>
    <section><h2>Contact</h2><p>${t.contact}</p></section>
  `;
}

// Events
languageSwitcher.addEventListener("change", e => loadContent(e.target.value));
loadContent("en"); // default
